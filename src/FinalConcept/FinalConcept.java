package FinalConcept;

public class FinalConcept {

	public static void main(String[] args) {
		
		int i = 10;
		i = 20;
		i = 30;
		
		System.out.println(i);
		
		final int j = 50; //Final Keyword is used to define constant values
		// j = 60; --> For variable declared as FINAL it's value cannot be modified
		
		System.out.println(j);	

	}

}
